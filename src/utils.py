def preprocessing_text(text):
    text = text.lower()
    text = text.replace('\n', ' ')
    text = text.replace('\\"', '\"')
    text = text.replace('RT', '')
    text = text.replace('\u0026', '&')
    
    for emoji in emoji_map.keys():
        text = text.replace(emoji, emoji_map[emoji])

    return text
